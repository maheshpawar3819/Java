
public class LoopsWithArray {
   public static void main(String[] args) {
	   int num[]=new int[5];
	   num[0]=10;
	   num[1]=15;
	   num[2]=14;
	   num[3]=11;
	   num[4]=20;
	   
	   for(int i=0;i<num.length;i++) {
		   System.out.println(num[i]);
	   }
	   
	   String names[]=new String[5];
	   names[0]="mahesh";
	   names[1]="balram";
	   names[2]="shrinath";
	   names[3]="rohan";
	   names[4]="pratik";
	   
	   for(int i=0;i<names.length;i++) {
		   System.out.println(names[i]);
	   }
   }

}
