
public class DataTypes {

	public static void main(String[] args) {
		//Int : it will contain Integer numbers like 542 -457;
		int number=50;
		//print
		System.out.println(number);
		
		//float : it will contain floating numbers with decimal values 12.451 
		float floatnum=75.25f;
		System.out.println(floatnum);
		
		//char : it will contain single character 'a'
		char character='a';
		System.out.println(character);
		
		//boolean : it will contain true or false 
		boolean bool=true;
		System.out.println(bool);
		
		//String : it will contain String like "mahesh"
		String name="Mahesh";
		System.out.println(name);
	}	

}
